// Type conversion - typecasting - vs type coersion

// alteração de um tipo de dado para ouro tipo


//type conversion - typecasting é quando nós fazemos explicitamente uma conversão de valores,
//type coersion é quando o JS fazisso de forma automatica para evitar erros nos códigos.

console.log("9" + 5) // neste caso o JS usará type coersion para dar 95, tendo então
//transformado o "9" em um number.

//se usarmos console.log(("9") + 5), aí estamos fazendo isso manualmente, de forma explícita,
// então seria um typecasting.

